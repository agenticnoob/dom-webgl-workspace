#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import ts from "typescript";

export const publicEntrypoints = [
  ["@viselora/dom-webgl", "packages/dom-webgl-runtime/dist/index.d.ts"],
  ["@viselora/dom-webgl/react", "packages/dom-webgl-runtime/dist/react.d.ts"],
  ["@viselora/scroll-adapters", "packages/dom-webgl-scroll-adapters/dist/index.d.ts"],
  ["@viselora/scroll-adapters/react", "packages/dom-webgl-scroll-adapters/dist/react.d.ts"],
];

const outputPath = "skills/viselora-dom-webgl/references/api-surface.generated.md";

export function collectPublicApi(root) {
  const runtimePackage = readJson(root, "packages/dom-webgl-runtime/package.json");
  const adaptersPackage = readJson(root, "packages/dom-webgl-scroll-adapters/package.json");
  if (runtimePackage.version !== adaptersPackage.version) {
    throw new Error(
      `Package version mismatch: ${runtimePackage.version} != ${adaptersPackage.version}`,
    );
  }

  return {
    version: runtimePackage.version,
    entrypoints: publicEntrypoints.map(([entrypoint, declarationPath]) => ({
      entrypoint,
      declarationPath,
      exports: collectEntrypoint(root, entrypoint, declarationPath),
    })),
  };
}

export function renderApiSurface(api) {
  const lines = [
    "# Generated Public API Surface",
    "",
    "> Generated by `scripts/generate-api-surface.mjs` from built package declarations. Do not edit this file manually.",
    "",
    `Compatible package version: ${api.version}`,
    "",
  ];

  for (const entrypoint of api.entrypoints) {
    lines.push(
      `## \`${entrypoint.entrypoint}\``,
      "",
      "| Symbol | Kind | Declaration summary | Re-export origin |",
      "| --- | --- | --- | --- |",
    );
    const records = [...entrypoint.exports].sort(
      (left, right) =>
        kindOrder(left.kind) - kindOrder(right.kind) ||
        left.name.localeCompare(right.name),
    );
    for (const record of records) {
      lines.push(
        `| ${escapeTable(record.name)} | ${record.kind} | ${escapeTable(record.signature)} | ${escapeTable(record.origin ?? "")} |`,
      );
    }
    lines.push("");
  }

  return lines.join("\n");
}

function collectEntrypoint(root, entrypoint, declarationPath) {
  const absolutePath = resolve(root, declarationPath);
  const sourceText = readFileSync(absolutePath, "utf8");
  const sourceFile = ts.createSourceFile(
    absolutePath,
    sourceText,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TS,
  );
  const declarations = new Map();
  const importedOrigins = new Map();

  for (const statement of sourceFile.statements) {
    indexDeclaration(statement, declarations);
    if (ts.isImportDeclaration(statement) && ts.isStringLiteralLike(statement.moduleSpecifier)) {
      const bindings = statement.importClause?.namedBindings;
      if (bindings && ts.isNamedImports(bindings)) {
        for (const element of bindings.elements) {
          importedOrigins.set(element.name.text, statement.moduleSpecifier.text);
        }
      }
    }
  }

  const records = [];
  for (const statement of sourceFile.statements) {
    if (!ts.isExportDeclaration(statement) || !statement.exportClause) continue;
    if (!ts.isNamedExports(statement.exportClause)) continue;
    const exportOrigin =
      statement.moduleSpecifier && ts.isStringLiteralLike(statement.moduleSpecifier)
        ? statement.moduleSpecifier.text
        : undefined;
    for (const specifier of statement.exportClause.elements) {
      const localName = specifier.propertyName?.text ?? specifier.name.text;
      const declaration = declarations.get(localName);
      const typeOnly =
        statement.isTypeOnly ||
        specifier.isTypeOnly ||
        isTypeOnlyDeclaration(declaration);
      const signature = declaration
        ? summarize(declaration.getText(sourceFile))
        : summarize(specifier.getText(sourceFile));
      records.push({
        entrypoint,
        name: specifier.name.text,
        kind: typeOnly ? "type" : "value",
        signature,
        origin: exportOrigin ?? importedOrigins.get(localName),
      });
    }
  }

  return records;
}

function indexDeclaration(statement, declarations) {
  if (
    ts.isTypeAliasDeclaration(statement) ||
    ts.isInterfaceDeclaration(statement) ||
    ts.isClassDeclaration(statement) ||
    ts.isFunctionDeclaration(statement) ||
    ts.isEnumDeclaration(statement) ||
    ts.isModuleDeclaration(statement)
  ) {
    if (statement.name && ts.isIdentifier(statement.name)) {
      declarations.set(statement.name.text, statement);
    }
    return;
  }
  if (ts.isVariableStatement(statement)) {
    for (const declaration of statement.declarationList.declarations) {
      if (ts.isIdentifier(declaration.name)) {
        declarations.set(declaration.name.text, statement);
      }
    }
  }
}

function isTypeOnlyDeclaration(declaration) {
  return Boolean(
    declaration &&
      (ts.isTypeAliasDeclaration(declaration) ||
        ts.isInterfaceDeclaration(declaration)),
  );
}

function summarize(value) {
  const normalized = value.replace(/\s+/g, " ").trim();
  return normalized.length <= 240 ? normalized : `${normalized.slice(0, 239)}…`;
}

function escapeTable(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("`", "\\`");
}

function kindOrder(kind) {
  return kind === "value" ? 0 : 1;
}

function readJson(root, path) {
  return JSON.parse(readFileSync(resolve(root, path), "utf8"));
}

function parseCli(argv) {
  let root = resolve(fileURLToPath(new URL("../../..", import.meta.url)));
  let check = false;
  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];
    if (argument === "--check") {
      check = true;
    } else if (argument === "--root" && argv[index + 1]) {
      root = resolve(argv[index + 1]);
      index += 1;
    } else {
      throw new Error(
        "Usage: generate-api-surface.mjs [--check] [--root <path>]",
      );
    }
  }
  return { check, root };
}

function main() {
  let options;
  try {
    options = parseCli(process.argv.slice(2));
    const api = collectPublicApi(options.root);
    const rendered = renderApiSurface(api);
    const absoluteOutput = resolve(options.root, outputPath);
    let current = "";
    try {
      current = readFileSync(absoluteOutput, "utf8");
    } catch {
      // Missing generated output is ordinary drift in check mode.
    }
    if (options.check) {
      if (current !== rendered) {
        process.stderr.write(
          "api-surface.generated.md is stale; run npm run skill:api:generate\n",
        );
        process.exitCode = 1;
      }
      return;
    }
    if (current !== rendered) writeFileSync(absoluteOutput, rendered);
    const records = api.entrypoints.flatMap((entrypoint) => entrypoint.exports);
    const values = records.filter((record) => record.kind === "value").length;
    const types = records.length - values;
    process.stdout.write(
      `Generated Viselora API surface: ${values} values, ${types} types\n`,
    );
  } catch (error) {
    process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
    process.exitCode = 1;
  }
}

if (
  process.argv[1] &&
  import.meta.url === pathToFileURL(resolve(process.argv[1])).href
) {
  main();
}
